package co.gov.dafp.sigep2.utils;

import java.math.BigDecimal;

public class AuditoriaSeguridad {
	
	private BigDecimal codUsuario;
	private BigDecimal codUsuarioRol;
	/**
	 * @return the codUsuario
	 */
	public BigDecimal getCodUsuario() {
		return codUsuario;
	}
	/**
	 * @param codUsuario the codUsuario to set
	 */
	public void setCodUsuario(BigDecimal codUsuario) {
		this.codUsuario = codUsuario;
	}
	/**
	 * @return the codUsuarioRol
	 */
	public BigDecimal getCodUsuarioRol() {
		return codUsuarioRol;
	}
	/**
	 * @param codUsuarioRol the codUsuarioRol to set
	 */
	public void setCodUsuarioRol(BigDecimal codUsuarioRol) {
		this.codUsuarioRol = codUsuarioRol;
	}

}
